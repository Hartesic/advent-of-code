def find_searched_word(searched_word: str,
                       origin_line_idx: int,
                       origin_char_idx: int,
                       offset_line_idx: int = 0,
                       offset_char_idx: int = 0) -> int:
    searched_char_idx = max(abs(offset_line_idx), abs(offset_char_idx))
    if searched_char_idx == len(searched_word):
        return 1

    if (origin_line_idx + offset_line_idx < 0
            or origin_line_idx + offset_line_idx >= len(input_grid)
            or origin_char_idx + offset_char_idx < 0
            or origin_char_idx + offset_char_idx >= len(input_grid[
                origin_line_idx + offset_line_idx])):
        return 0

    searched_char = searched_word[searched_char_idx]

    if offset_line_idx == 0 and offset_char_idx == 0:
        if input_grid[origin_line_idx][origin_char_idx] != searched_char:
            return 0

        searched_word_count = 0
        for line_offset in range(-1, 2):
            for char_offset in range(-1, 2):
                if line_offset == 0 and char_offset == 0:
                    continue
                searched_word_count += find_searched_word(searched_word,
                                                          origin_line_idx,
                                                          origin_char_idx,
                                                          line_offset,
                                                          char_offset)
        return searched_word_count

    if input_grid[origin_line_idx + offset_line_idx][
            origin_char_idx + offset_char_idx] == searched_char:
        if offset_line_idx != 0:
            offset_line_idx += (1 if offset_line_idx > 0 else -1)
        if offset_char_idx != 0:
            offset_char_idx += (1 if offset_char_idx > 0 else -1)
        return find_searched_word(searched_word,
                                  origin_line_idx, origin_char_idx,
                                  offset_line_idx, offset_char_idx)

    return 0


def part_one(searched_word):
    searched_word_count = 0

    for line_idx, line in enumerate(input_grid):
        for char_idx in range(len(line)):
            searched_word_count += find_searched_word(searched_word,
                                                      line_idx, char_idx)

    return searched_word_count


