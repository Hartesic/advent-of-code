def part_two():
    expected_angle_chars = [
        ["M", "M", "S", "S"],
        ["M", "S", "M", "S"],
        ["S", "S", "M", "M"],
        ["S", "M", "S", "M"]
    ]
    x_mas_count = 0

    for line_idx in range(1, len(input_grid) - 1):
        line = input_grid[line_idx]
        for char_idx in range(1, len(line) - 1):
            char = line[char_idx]
            if char == 'A':
                angle_chars = [
                    input_grid[line_idx - 1][char_idx - 1],
                    input_grid[line_idx - 1][char_idx + 1],
                    input_grid[line_idx + 1][char_idx - 1],
                    input_grid[line_idx + 1][char_idx + 1],
                ]
                if angle_chars in expected_angle_chars:
                    x_mas_count += 1

    return x_mas_count


print("Occurence count of X-MASes:", part_two())
