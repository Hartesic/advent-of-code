LINE_UP = '\033[1A'
LINE_CLEAR = '\x1b[2K'


def print_word_map(origin_line_idx: int, origin_char_idx: int,
                   offset_line_idx: int, offset_char_idx: int,
                   searched_char: str):
    """
    Display the input while highlighting the character the current search
    started from and the current inspected character.

    Useful to see the algorithm in action.
    """
    selected_line = origin_line_idx
    selected_char = origin_char_idx
    highlighed_line = origin_line_idx + offset_line_idx
    highlighed_char = origin_char_idx + offset_char_idx

    print("Looking for char:", searched_char)

    for line_idx in range(len(input_grid)):
        for char_idx in range(len(input_grid[line_idx])):
            is_selected_char = (line_idx == selected_line
                                and char_idx == selected_char)
            is_highlighted_char = (line_idx == highlighed_line
                                   and char_idx == highlighed_char)
            char = input_grid[line_idx][char_idx]
            if is_selected_char:
                print(Fore.GREEN + char, end='')
            elif is_highlighted_char:
                print(Fore.YELLOW + char, end='')
            else:
                print(char, end='')
            print(Style.RESET_ALL, end='')
        print("\n", end='')

    sleep(.5)
    for _ in range(len(input_grid) + 1):
        print(LINE_UP, end=LINE_CLEAR)
    print('', end='')


